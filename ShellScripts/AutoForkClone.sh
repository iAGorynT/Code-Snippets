#!/bin/zsh
# AutoForkClone.sh
# Automates GitHub fork â†’ clone â†’ setup (with optional feature branch)
# Repos are cloned into $HOME/Documents/GitHub

# Source function library with error handling
FORMAT_LIBRARY="$HOME/ShellScripts/FLibFormatPrintf.sh"
[[ -f "$FORMAT_LIBRARY" ]] || { printf "Error: Required library $FORMAT_LIBRARY not found" >&2; exit 1; }
source "$FORMAT_LIBRARY"

# --- CONFIG ---
BASE_DIR="$HOME/Documents/GitHub"

# --- FUNCTIONS ---
usage() {
  cat << EOF
Usage: $0 [<github-repo-url>] [feature-branch-name]

If no URL is provided, you'll be prompted to enter one.

Examples:
  $0 https://github.com/someuser/project
  $0 https://github.com/someuser/project feature/update-readme
  $0  # Interactive mode - prompts for URL

EOF
  exit 1
}

validate_url() {
  local url="$1"
  if [[ ! "$url" =~ ^https://github\.com/[^/]+/[^/]+/?$ ]]; then
    error_printf "Invalid GitHub URL format. Expected: https://github.com/username/repo"
    return 1
  fi
  return 0
}

check_gh_auth() {
  if ! gh auth status &>/dev/null; then
    error_printf "Not authenticated with GitHub CLI. Please run: gh auth login" true
  fi
}

create_setup_summary() {
  local summary_file="$LOCAL_PATH/SETUP_SUMMARY.md"
  local timestamp=$(date '+%Y-%m-%d %H:%M:%S')
  
  cat > "$summary_file" << EOF
# Setup Summary

**Generated:** $timestamp

## Repository Information

- **Local Path:** \`$LOCAL_PATH\`
- **Fork URL:** $FORK_URL
- **Upstream URL:** $REPO_URL
- **Repository Name:** $REPO_NAME
- **GitHub User:** $GITHUB_USER
$(if [[ -n "$FEATURE_BRANCH" ]]; then echo "- **Feature Branch:** \`$FEATURE_BRANCH\`"; fi)

## Git Remotes

\`\`\`
$(git remote -v)
\`\`\`

## Next Steps

### 1. Navigate to repository
\`\`\`bash
cd "$LOCAL_PATH"
\`\`\`

### 2. Make your changes

### 3. Commit and push
\`\`\`bash
git add .
git commit -m "Your descriptive commit message"
git push origin ${FEATURE_BRANCH:-main}
\`\`\`

### 4. Create a pull request
\`\`\`bash
gh pr create --web
\`\`\`

---
*This file was automatically generated by AutoForkClone.sh*
EOF

  success_printf "Setup summary saved to: $summary_file"
}

# --- MAIN ---
clear
format_printf "Auto Fork and Clone..." "yellow" "bold"
printf "\n"
[[ "$1" == "-h" || "$1" == "--help" ]] && usage

# Check GitHub CLI authentication first
check_gh_auth

# Get GitHub username
GITHUB_USER=$(gh api user --jq .login 2>/dev/null)
if [[ -z "$GITHUB_USER" ]]; then
  error_printf "Failed to get GitHub username. Check your authentication." true
fi

# Get repo URL (from argument or prompt)
if [[ -z "$1" ]]; then
  read -r "REPO_URL?Enter the GitHub repository URL to fork: "
  REPO_URL="${REPO_URL## }"  # Trim leading whitespace
  REPO_URL="${REPO_URL%% }"  # Trim trailing whitespace
  [[ -z "$REPO_URL" ]] && { error_printf "No URL provided" true; }
else
  REPO_URL="$1"
fi

# Remove trailing slash if present
REPO_URL="${REPO_URL%/}"

# Validate URL
validate_url "$REPO_URL" || exit 1

# Parse repo information
REPO_NAME=$(basename "$REPO_URL" .git)
ORIGINAL_USER=$(echo "$REPO_URL" | sed -E 's#https://github\.com/([^/]+)/.*#\1#')

# Check if forking your own repo
if [[ "$ORIGINAL_USER" == "$GITHUB_USER" ]]; then
  read -r "response?âš ï¸  This is already your repository. Do you want to just clone it? (y/n) "
  if [[ "$response" =~ ^[Yy]$ ]]; then
    FORK_URL="$REPO_URL"
